package cz.vkr.dto;

public class Vitr {

	
	private int smer;
	private int rychlost;
	
	
	
	
	
	
	
	
	public Vitr(int smer, int rychlost) {
		super();
		this.smer = smer;
		this.rychlost = rychlost;
	}
	public int getSmer() {
		return smer;
	}
	public void setSmer(int smer) {
		this.smer = smer;
	}
	public int getRychlost() {
		return rychlost;
	}
	public void setRychlost(int rychlost) {
		this.rychlost = rychlost;
	}
	
	
	
	
	
}
